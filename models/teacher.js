class Teacher {
    constructor(id, description, name, username, email, groups, subjects, school) {
      this.id = id;
      this.description = description;
      this.name = name;
      this.username = username;
      this.email = email;
      this.groups = groups;
      this.subjects = subjects;
      this.school = school;
    }
  }
  
  export default Teacher;